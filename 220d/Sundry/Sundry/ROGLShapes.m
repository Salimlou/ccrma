@interface ROGLShapes ()
{
    GLuint cubeVertexBuffer;
}
@end

@implementation ROGLShapes

+ (ROGLShapes *)sharedInstance
{
    static ROGLShapes *sharedInstance;
    if (sharedInstance == nil)
    {
        sharedInstance = [[ROGLShapes alloc] init];
    }
    return sharedInstance;
}

- (id)init {
    self = [super init];
    if (self) {
        glGenBuffers(1, &cubeVertexBuffer);
        glBindBuffer(GL_ARRAY_BUFFER, cubeVertexBuffer);
        glBufferData(GL_ARRAY_BUFFER, sizeof(gCubeVertexData), gCubeVertexData, GL_STATIC_DRAW);
        
        glEnableVertexAttribArray(GLKVertexAttribPosition);
        glVertexAttribPointer(GLKVertexAttribPosition, 3, GL_FLOAT, GL_FALSE, 24, BUFFER_OFFSET(0));
        glEnableVertexAttribArray(GLKVertexAttribNormal);
        glVertexAttribPointer(GLKVertexAttribNormal, 3, GL_FLOAT, GL_FALSE, 24, BUFFER_OFFSET(12));
    }
    return self;
}

- (void)dealloc {
    glDeleteBuffers(1, &cubeVertexBuffer);
}

- (void)prepareToDrawCubes
{
    glBindBuffer(GL_ARRAY_BUFFER, cubeVertexBuffer);
    glEnableVertexAttribArray(GLKVertexAttribPosition);
    glVertexAttribPointer(GLKVertexAttribPosition, 3, GL_FLOAT, GL_FALSE, 24, BUFFER_OFFSET(0));
    glEnableVertexAttribArray(GLKVertexAttribNormal);
    glVertexAttribPointer(GLKVertexAttribNormal, 3, GL_FLOAT, GL_FALSE, 24, BUFFER_OFFSET(12));
}

- (void)drawCube
{
    glDrawArrays(GL_TRIANGLES, 0, 36);
}

- (void)prepareToDrawTriangles
{
    glBindBuffer(GL_ARRAY_BUFFER, 0);
}

- (void)prepareToDrawSpringThings
{
    glBindBuffer(GL_ARRAY_BUFFER, 0);
}

@end
